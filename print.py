print("hello world","Rudra",6)
#by default after comma space is added
print("Hello World")
print("Rudra")
#by default after print cursor moves to next line
#We can choose for these two print function behaviours by using sep and end parameters
print("Hello World","Rudra",6,sep="")#we are putting no space between these three values
print("Hello World","Rudra",6,sep="---")#we are putting --- between these three values
print("Hello World",end=",")#we are putting , at the end instead of new line")
print("Rudra")